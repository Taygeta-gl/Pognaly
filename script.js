function createSparklesAround(element, count = 8) {
  const rect = element.getBoundingClientRect();
  
  // Center coordinates of #pp
  const centerX = rect.left + rect.width / 2;
  const centerY = rect.top + rect.height / 2;
  
  // Radius for sparkles ring - a bit outside the circle's radius
  const radius = rect.width / 2 + 20; // 20px outside border
  
  for (let i = 0; i < count; i++) {
    const sparkle = document.createElement('div');
    sparkle.classList.add('sparkle');
    
    // Calculate angle for each sparkle (evenly spaced)
    const angle = (i / count) * 2 * Math.PI;
    
    // Calculate sparkle positions on the ring
    const x = centerX + radius * Math.cos(angle);
    const y = centerY + radius * Math.sin(angle);
    
    sparkle.style.left = x + 'px';
    sparkle.style.top = y + 'px';
    
    document.body.appendChild(sparkle);
    
    setTimeout(() => sparkle.remove(), 1000);
  }
}





